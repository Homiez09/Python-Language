'''
14.เขียนโปรแกรมรับ input 2 ตัว ที่เป็นจำนวนเต็ม และตรวจสอบว่า ผลบวกของจำนวนทั้งสองมีค่ามากกว่า 0, น้อยกว่า 0, หรือเท่ากับ 0
    -ถ้า > 0 ให้พิมพ์ a+b>0
    -ถ้า < 0 ให้พิมพ์ a+b<0
    -ถ้า = 0 ให้พิมพ์ a+b=0
'''

a = int(input('Input A : '))
b = int(input('Input B : '))

if(a + b > 0):
    print(a, '+', b, '>', '0')
elif(a + b < 0):
    print(a, '+', b, '<', '0')
else:
    print(a, '+', b, '=', '0')

