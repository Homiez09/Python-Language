'''
3.เขียนโปรแกรมรับค่า input 1 ตะว ที่เป็นจำนวนเต็ม และตรวจสอบว่าจำนวนที่รับมามีค่ามากกว่า 0, น้อยกว่า 0, เท่ากับ 0
    -ถ้ามีค่ามากกว่า 0 ให้พิมพ์ > 0
    -ถ้ามีค่าน้อยกว่า 0 ให้พิมพ์ < 0
    -ถ้ามีค่าเท่ากับ 0 ให้พิมพ์ = 0
'''
number = int(input('Number : '))
if number > 0:
    print('> 0')
if number < 0:
    print('< 0')
if number == 0:
    print('= 0')